#include "hc05.h"

void HC05_Init(void)
{
    /********************************************
        Enable Clock
    ********************************************/
    RCC->AHB1ENR |= (1<<0);
    RCC->APB2ENR |= (1<<4);

    /********************************************
        PA9 = TX , PA10 = RX
    ********************************************/
    GPIOA->MODER &= ~((3<<18) | (3<<20));
    GPIOA->MODER |=  ((2<<18) | (2<<20));

    /********************************************
        AF7 USART1
    ********************************************/
    GPIOA->AFR[1] &= ~((15<<4) | (15<<8));
    GPIOA->AFR[1] |=  ((7<<4) | (7<<8));

    /********************************************
        Baud = 9600
    ********************************************/
    USART1->BRR = 0x0196;

    /********************************************
        Enable TX, RX và USART1
    ********************************************/
    USART1->CR1 |= ((1<<3) | (1<<2) | (1<<13));
}

void HC05_SendChar(char c)
{
    while(!(USART1->SR & (1<<7)));
    USART1->DR = c;
}

void HC05_SendString(char *str)
{
    while(*str)
    {
        HC05_SendChar(*str++);
    }
}
