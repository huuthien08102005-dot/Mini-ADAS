#include "buzzer.h"

extern void delay_ms(uint32_t time);

/************************************************
                INIT PA6
************************************************/

void Buzzer_Init(void)
{
    /********************************************
        Enable GPIOA Clock
    ********************************************/

    RCC->AHB1ENR |= (1<<0);

    /********************************************
        PA6 OUTPUT
    ********************************************/

    GPIOA->MODER |= (1<<12);
}

/************************************************
                BUZZER ON
************************************************/

void Buzzer_ON(void)
{
    GPIOA->ODR |= (1<<6);
}

/************************************************
                BUZZER OFF
************************************************/

void Buzzer_OFF(void)
{
    GPIOA->ODR &= ~(1<<6);
}

/************************************************
        DISTANCE WARNING SYSTEM
************************************************/

void Buzzer_Control(int distance)
{
    /********************************************
                    SAFE
    ********************************************/

    if(distance > 50)
    {
        Buzzer_OFF();
    }

    /********************************************
                    DANGER
    ********************************************/

    else
    {
        int delay_time;

        /****************************************
            distance giảm → beep nhanh hơn
        ****************************************/

        delay_time = distance * 8;

        /****************************************
            Giới hạn nhỏ nhất
        ****************************************/

        if(delay_time < 20)
        {
            delay_time = 20;
        }

        Buzzer_ON();

        delay_ms(delay_time);

        Buzzer_OFF();

        delay_ms(delay_time);
    }
}
